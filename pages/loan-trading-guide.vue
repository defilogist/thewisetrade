<h1>The Loan Trading Guide</h1>

<p>
Loaning can sound very fuzzy at first sight. Nevertheless, it’s a very handy tool that can improve your trading, especially when you want to be $SOL liquid while keeping your favourite NFTs.
</p>

<p>
That said, it is very risky tool. By giving you more flexibility, it will add volatility to your trades. If it’s not used properly, that can ruin all your holdings. So you should use it with extreme caution and think a lot before taking a loan. I recommend loans only for experimented traders.
</p>

<p>
Now you are aware of the situation, In this guide I’m going to tell you how borrowing can be used efficiently. I will cover too lending to see how to manage it properly.
</p>


<h2>Main principle</h2>

<p>
A lender proposes an amount of $SOLs to lend to a borrower but asks a NFT as a collateral in case the borrower can’t pay. It means that if one borrows the $SOLs (which are always lower than the NFT Floor price) and don’t pay back, the lender gets the NFT instead of the $SOL lent. Most of the time, payment default will be like instant selling your NFT at a discounted price.
</p>

<p>
All loans have a limited duration and come with some interests. These parameters are set by the lender, The lender earns money through the interest. Duration varies between 3 and 21 days and interest are between 2 and 3% for 7 days.
</p>

<p>
The interesting point is that if you default, you lose only your NFT, you don’t owe anything to anyone.
</p>


