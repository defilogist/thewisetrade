  <h1>NFT Trading guide basics</h1>

  <h2>Fundamentals</h2>

  <ul>
    <li>
      To become a good trader you have to practice and make mistakes (you will
      lose money during the process).
    </li>
    <li>
      It requires much more patience than you think. Many collections took
      months to take off.
    </li>
    <li>
      You have to understand the market to take good positions. It requires to
      spend time on marketplaces and on collection Twitters and Discords.
    </li>
    <li>
      Day trading is a full-time job, so if it’s not your main occupation, don’t
      look for quick wins:
    </li>
    <li>
      It’s a very irrational market where hype plays a lot in the floor price.
      See collections as fashion brands. You cannot ignore the hype effect in
      your investments.
    </li>
    <li>
      Trade only with a good internet connection. Some orders could be missed
      and in some situation you need reactivity.
    </li>
  </ul>

  <h2>Listing</h2>

  <ul>
    <li>
      Identify listing walls and list your NFTs slightly below to ensure its
      selling.
    </li>
    <li>
      If the collection is dumping you have to list significantly below the
      floor to sell quickly.
    </li>
    <li>If the collection is pumping or stable, list it around the floor.</li>
    <li>Clean NFTs tend be sold faster but not at a higher price.</li>
    <li>
      Listing is great for settling your selling strategy. But it will prevent
      you from getting perks.
    </li>
    <li>If you are holding on the long term it’s better to not list.</li>
  </ul>

  <h2>Holding</h2>

  <ul>
    <li>
      Staking makes sense only if you wants to keep the NFTs on a long term. If
      that’s not the case, don’t let it impact your investment strategy.
    </li>
    <li>
      Many major collections have a sub collection. It’s better to buy the subs
      since you can split the sell more easily.
    </li>
    <li>
      It’s better to buy several NFTs from a collection and sell them gradually.
    </li>
    <li>
      When you do gradual sells, keep a few NFT from your conviction play in
      your long-term inventory.
    </li>
    <li>Or buy a few high conviction NFTs when you make big wins.</li>
    <li>
      Bitcoin took 10 years to reach 60k, so it’s not absurd to hold some NFTs a
      few years.
    </li>
  </ul>

  <h2>Taking Profits</h2>

  <ul>
    <li>X2 or X3 are already very good profits.</li>
    <li>Every time you are in green, be satisfied. Don’t look back.</li>
    <li>x1.2 can already be very good if the initial investment is heavy.</li>
    <li>x1.2 is better than a negative move.</li>
    <li>
      Take some profit in FIAT, it’s good to get rewards from time to time
    </li>
  </ul>

  <h2>Risk Management</h2>

  <ul>
    <li>
      If a NFT makes a significant pump a day or two after you buy it, sell it.
      Most of the time it will dump the following days (and buy it again if you
      believe in it).
    </li>
    <li>
      Sell when it pumps. During bull runs you should be more and more liquid.
    </li>
    <li>
      Hold when it dumps. Or cut your loss at bad price to be sure the NFT is
      sold if you consider that the collection is dead. Selling during dumps is
      harder.
    </li>
    <li>
      Selling requires collection activity. Before buying from a collection,
      consider this because it can make your expected sale much slower.
    </li>
    <li>
      During bear cycles, use your liquidity gradually to buy underpriced
      collections. It’s normal to be less and less liquid over time.
    </li>
    <li>
      During the bear if something goes up, sell at x2 or x3, don’t wait for
      better performances.
    </li>
    <li>
      Most NFTs success are based on good marketing from the team. So if you are
      already heavily in profit, consider to sell a part of them. The marketing
      can fade quickly.
    </li>
    <li>
      Leaving a collection while making profit doesn’t mean you can’t come back.
      You will be able to buy back the NFTs during the next bear cycle.
    </li>
    <li>
      It may sound counterintuitive but selling a Degods at 40 or 50 during
      their pump was a great decision if it was a significant part of the holder
      portfolio. It secured gains and didn’t bet on the risky scenario that it
      would go higher.
    </li>
    <li>Use a different wallet than your holdings when minting.</li>
    <li>Keep a significant percentage of your wallet in sols.</li>
  </ul>

  <h2>Do your own research</h2>

  <ul>
    <li>
      Before investing in a collection you should check the collection website,
      Twitter and Discord
    </li>
    <li>Bad art, poor website, unactive twitters are bad signals</li>
    <li>
      Discord with low activity or with floor price discussions only are bad
      signals
    </li>
    <li>
      Services attached to the NFT like software or goodies are good signals
    </li>
    <li>
      Active communication from the team and good vibe from the community are
      good signals
    </li>
    <li>A high proportion of listing is a bad signal</li>
    <li>Absence of volume may be a bad signal (lack of activity).</li>
    <li>
      Small collections can have low activity but in that case, it’s preferable
      they have a low listing rate.
    </li>
    <li>
      Getting in early into a collection doesn’t mean getting in super early.
      For instance, you could have entered confidently in the Famous Foxes when
      they were selling at 10 sols. At that time they already proved their
      value. It would have been better to spot them earlier but 10 was still a
      good entry point.
    </li>
  </ul>

  <h2>Alpha calls</h2>

  <ul>
    <li>Alphas are signals, they don’t guarantee anything.</li>
    <li>Most of them are wrong.</li>
    <li>They are emitted by people who follows the market.</li>
    <li>
      But some alpha callers are tempted to dump their bag taking advantage of
      their influence.
    </li>
    <li>
      Alpha callers brag loud when they win, they don’t say anything when they
      miss their targets.
    </li>
    <li>Some alpha callers are good and some are not. Do your research on them too.</li>
    <li>See alpha calls as market data not advice.</li>
  </ul>

  <h2>Casino projects</h2>

  <p>
    Casinos are bad for many reasons. Even if they can unsure a quick flip, you
    should avoid it because :
  </p>

  <ul>
    <li>They drain wallets of Solana users and can destroy lives.</li>
    <li>They don’t give a good image of the NFT market when newcomers come.</li>
    <li>They don’t bring any value.</li>
    <li>They rug often.</li>
    <li>On the long term they mine the ecosystem.</li>
  </ul>

