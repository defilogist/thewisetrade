<h3>Lending</h3>

<h3>When to lend?</h3>

<p>
The main benefit of lending is obvious: it allows to get interests from the borrower. On average it leads to 200-300% APR, which is huge compared to Solana validators (5-10%).
</p>

<p>
To set a lend you have to decide several parameters: the platform you use, the amount of $SOL loanedt the duration, the interest rate, the amount lent and the NFT used as collateral.
</p>

<p>
Be aware that the platform will get some fees. Some platform has better rate than others but overall they are in an decent range.
</p>

<h3>What deals can you propose?</h3>

<p>
You can look at other offers to be sure of what parameters you should chose. Once you set your deal, you just have to do the maths (platform helps you with that) and see if it’s worth the risk for you.
</p>

<p>
The deal has to be a bit far from floor to avoid payment defaults in case the collection dumps a bit. But it must not be too far, because it would make the loan not interesting for the borrower. On average proposals are between 15 and 20% below the floor. Check the offers made by other users, if they are better they will be selected before yours. Keep in mind that your deal must be accepted, it needs to be attractive.
</p>

<p>
Platform represents the above concept as LTV. It’s the pourcentage of the floor price you lend.
</p>

<p>
The other thing you can set is the interest rate which is usually around 2.5% for 7 days. Platform will present you with the form of APR but they will do maths with other parameters to show how much interests you gain in the end. You can set longer loans too, like 14 days or 21 days, the interest rate evolve accordingly.
</p>

<p>
The bottom line is your loan parameter depends on your patience, you are on a market with competitors. The more attractive the offer the faster it goes but the less the rewards you get.
</p>

<p>
NB: When popular mint occur, a lot of people are looking for liquidity. During this period you can make less attractive offers like 10% below floor with stronger interest rate.
</p>

<h3>How to deal with payment default ?</h3>

<p>
The payment default allows you to get a NFT at discount price. It can occur for two reasons:
</p>

<p>
- The borrower made bad trades and cannot pay back the loan. That’s a good situation, you get a NFT below its floor price.
- The collection you lended on dumped, the lender chosed to not repay his loan and you get the NFT at a higher price than the current floor price.
</p>

<p>
To avoid the second case, there is no magic recipe. The only thing you can do is to give loans only on collections you are ready to hold in case they dump. You can focus too on solid and stable collections to put the probabilities on your side.
</p>

<h3>Sum up</h3>

<p>
**Upside**: getting great APR for $SOL, acquiring NFTs at cheap price
</p>

<p>
**Downside**: Can end with an expensive NFT
</p>

<h2>Mortgage</h2>

<h3>What makes mortgages different than loans?</h3>

<p>
Mortgages are leverage tools for borrowers. From the lender point of view, it doesn’t change anything.
</p>

<p>
As a borrower, mortgages allow you to buy a NFT without having the liquidity yet. It’s a bet on the fact that a collection will pump soon. You buy a NFT with the difference between the floor price and royalties and the best loan offer. It allows you to get the NFT right now while you are still missing the liquidity to buy it. But, the NFT is frozen and can’t be traded until you pay the loan back.
</p>

<p>
The loan can be paid via an instant sell of the NFT. So, if things goes well you can trade out of your range NFT without having the liquidity for this. If you default, the initial money is lost and the NFT is given.
</p>

<p>
That’s still a risky tool. Its main utility is to allows you to buy several NFTs with less money, and it allows to add a multiplicator to your trades by buying more NFTs at the same time. In your previson, if you go for multiple NFTs, include the fact that royalty costs are multiplied too.
</p>

<h3>Exemple</h3>

<p>
Let’s take an example to make things clearer:
</p>

<p>
- At the time of the writing I can buy an Okay Bear mortgage at 18.5 $SOL with a Floor price at 75.
NB: If I had 80 $SOL in my wallet I could buy 4 Bears.
- If I take a mortgage, the Okay bear is now in my wallet and give me holder access to the Okay bear community. But, I get a loan of 61 $SOL and the NFT can’t be traded until I pay the loan.
- If the price goes up to 95, I can decide to fund the money borrowed, unlock the NFT and sell it on a market for a 20 $SOL gain (minus royalties and loan interests).
- If the price goes down at 65, I lose the 10 $SOL (plus royalties and loan interests)
- If I can’t find the missing liquidity I lose 18.5 $SOL.
- If the floor dumps harder like at 55, I can default my payment and lose 18.5 $SOL instead of 20.
</p>

<h3>To sum up</h3>

<p>
**Upside**: buying big NFT without being liquid enough, trading more NFTs at the same time
</p>

<p>
**Downside**: can lose the initial investment, loss is harder, add a layers of complexity on top of the loan
</p>

