<h3>What are the deal parameters?</h3>

A lender sets:

<ul>
<li>How much sols he can furnish</li>
<li>The NFT collection to use as collateral</li>
<li>The duration (3, 5, 7, 14 or 21 days)</li>
<li>The intest rate (between 2 and 10% depending on the duration).</li>
</ul>

<h3>How platforms work?</h3>

<p>
They work in a similar way: lenders list deals and borrowers can pick the deals they are interested in. Borrowers can propose deals too and be picked, it’s like a classic trading order book (the way deals work is described in the next paragraphs).
</p>

<p>
NB: Some offers can be sent privately. They are dealt on a separate chat platform and the lender set it for a singe wallet via one of the lending platform.
</p>

<h2>Borrowing</h2>

<h3>When to borrow?</h3>

<p>
Basically you borrow $SOL when you need liquidites for a trade. It’s often for a mint of which the outcome will be known quickly or for investing in collections that will likely pump soon (bullish announcement, incoming airdrop for collection holders or similar). In both cases you will have to cash out quickly to pay back your loans and avoid activating the collateral NFT selling. Loans are mainly for short term investments.
</p>

<p>
My recommandation is to borrow 30% maximum of your current holding. That way, if things go wrong, you will still be able to pay back the borrowing. The other option to limit risks is to be ready to lose the NFTs put as collateral.
</p>

<p>
Note that loaning can be good to enforce a trading strategy. They force you to cash out your trades at a given time. It avoids holding the NFT with false hope that its floor gets higher.
</p>


<h3>What is a good deal?</h3>

<p>
Interests amount is most of the time not significant compared to the expected gains of your upcoming trade. They should be considered but if the amount is a problem, it means that the deal is not that good or that there is an issue with your strategy.
</p>

<h3>Be Liquid</h3>

<p>
Another option to mitigate risks is to spend only the money you borrowed. So, you make sure you can pay it back in case of big loss. You still have your liquidity available in case of sudden new opportunities.
</p>

<p>
You are always liquid, meaning you have $SOLs avalaible in your wallet. You mainly invest money from others for your short term trades.
</p>

<h3>Set a stop loss</h3>

<p>
Another usage for borrowing is to set a stop loss on a NFT you are not sure of. You bought it at floor price and bet on the price volatility. To manage your risks, you want a bottom exit price. There is no tool for that (except AMMs but they are complicated to use, I will cover them in another guide), so you can put the NFT as collateral of a borrowing. It will make you more liquid while setting your stop loss. But you will have to pay interests for this service if the stop loss is not activated.
</p>

<h3>Warning</h3>

<p>
Be aware of the gambler effect where you put all your NFTs as collateral and lose them one by one after many short terms failed opportunities. It could ruin your bag very fast and hurt your mental health.
</p>

<h3>Sum up</h3>

<p>
**Upside**: getting more liquidity, always be liquid, better investment strategy with stop-loss, enforce exits.
</p>

<p>
**Downside**: risk to lose NFT if your investments goes wrong or if you forget to pay back. Possible addiction that could ruin youn portfolio
</p>

