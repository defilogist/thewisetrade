<h2>FOMO</h2>

<ul>
  <li>
    The Fear Of Missing Out is the hardest feeling to manage.
  </li>
  <li>
    The FOMO will make you buy when the floor price already climbed. If something already pumps, take time to push your analyze of the situation. During a bear market, it’s too late 99% of the time. During a bull, it’s trickier but most of the time it will be a bad play.
  </li>
  <li>
    When a collection you like goes up, you will feel the urgency to buy. You buy it at a price you would have never done a few days ago. This is the FOMO.
  </li>
  <li>
    When Many people will feel like you, the price will increase significantly. Enough for the whales (the one who bought when the FP was low) to start selling their NFTs. Which will slow down the floor price increase. At that time people will be scared and list massively their NFTs and cause the floor to go down bad. You lose money after an impulsive buy in.
  </li>
  <li>
    There is the FOMO of missing the next Degods you still don’t have, but there is the FOMO of not to sell the Degods you think you have. This one is even more problematic.
  </li>
  <li>
    The FOMO will push you to hold poor collections or to never sell great collections that will have up and down.
  </li>
  <li>
    Keep in mind that when you’re in profit if you sell you can’t lose: you will be able to buy again later the same collection or getting in other new opportunities.
  </li>
  <li>
    If you really feel that a collection will do great over time, you can buy several NFTs of it, sell it gradually and keep 1 whatever happens. And buy them again when it dumps.
  </li>
  <li>
    Keep for x10, x100 only very high conviction play.
  </li>
</ul>

